	cout << "***************************************" << endl;
	
	// Difference
	cout << "***************************************" << endl;
	cout << "F I N D I N G    D I F F E R E N C E" << endl;
	cout << "Enter the number: ";
	cin >> a;
	cout << "Enter the number: ";
	cin >> b;
